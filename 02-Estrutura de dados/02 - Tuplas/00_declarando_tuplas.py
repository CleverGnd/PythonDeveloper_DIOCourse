# Aula 01: Tuplas

# Tuplas são imutáveis, diferente das listas que são mutáveis

# Criando uma tupla usando parênteses
frutas = (
    "laranja",
    "pera",
    "uva",
)
print(frutas)

# Criando uma tupla usando a função tuple()
letras = tuple("python")
print(letras)

numeros = tuple([1, 2, 3, 4])
print(numeros)

# Criando uma tupla com um único elemento
pais = ("Brasil",)
print(pais)
